function bankposw(bankM){
	var None = false;
	//显示后4位
	var bankLast = bankM.substring(bankM.length-4,bankM.length);
	var bankHtml = "";
	for( var i = 0; i<bankM.length-4; i++){
		bankHtml+="*";
	}
	bankHtml+=bankLast;

	//前六位匹配银行 ico
	$.ajax({
         type:'post',
         cache:false,
         url:'<?php echo base_url(); ?>assets/js/bankData .json',
         dataType:'json',
         success:function(data){
         	for(var i = 0 ; i<data.length;i++){
         		if(data[i].bin == bankM.substring(0,data[i].bin.length)){
         			console.log(data[i].bankName);
         			BankName (data[i].bankName);
         			jQuery(".bankress").html(data[i].bankName)
         			None = true;
         		}
         	}
         	if(None == false){
         		BankName ("none");
         	}
         }
     });
     return bankHtml;
}

